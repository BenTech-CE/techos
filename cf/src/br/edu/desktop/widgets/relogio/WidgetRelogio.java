    public WidgetRelogio() {
        initComponents();
        
        putClientProperty(FlatClientProperties.STYLE, "arc: 35");
        
        Font sf = FuncoesUteis.carregarFonte("/br/edu/desktop/fonts/sf-ns-compressed-bold.ttf");
        sf56 = sf.deriveFont(Font.PLAIN, 56);
        sf48 = sf.deriveFont(Font.PLAIN, 48);
        
        jLabel1.setFont(sf56);
        
        formatadorData = DateTimeFormatter.ofPattern("H:mm");
        
        atualizarHorario();
        
        // Mudando o horÃ¡rio a cada 1 segundo.
        horarioAutomatico = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                atualizarHorario();
            }
        });
        
        horarioAutomatico.start();
    }
    
    public void atualizarHorario () {
        LocalDateTime data = LocalDateTime.now();
                
        String novoTitulo = formatadorData.format(data);
                
        if (novoTitulo.length() > 4) {
            jLabel1.setFont(sf48);
        } else {
            jLabel1.setFont(sf56);
        }
            
        jLabel1.setText(novoTitulo);        
    }

